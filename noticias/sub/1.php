        <h1><span class="goldText">Camila Fernández</span> firma contrato con universal music para celebrar su cumpleaños 18</h1>
    </div>

    <div class="newsBody">
        <p>
            México, D.F. a 30 de noviembre del 2015.– Este lunes 30 de noviembre Camila Fernández, hija de Alejandro Fernández y nieta de Don Vicente Fernández, celebró su cumpleaños número 18 y lo hizo de una manera única: firmó con Universal Music para dedicarse de manera profesional a la música, siguiendo el linaje de la familia Fernández.

            
        </p>

        <p>
            Camila invitó a su familia y medios de comunicación para que compartieran con ella este momento histórico en una cálida conferencia de prensa. Además, decidió sorprender a todos los asistentes pues de manera inesperada cantó una canción de manera acústica con su guitarra. Entre los asistentes estaban su mamá América Guinart, su hermana América Fernández y su hermano Alejandro Fernández Jr., quienes brindaron y festejaron con ella después de la canción.
        </p>

        <p>
            Más tarde se procedió a la firma del contrato con ejecutivos de Universal Music. Alejandro Fernández, su padre, estuvo presente apoyándola al igual que toda su familia. Por el momento, Camila está en la búsqueda de canciones y productores para su primer material. Sin embargo, adelantó que para enero de 2016 tiene una sorpresa preparada.
        </p>

        <p>
            "Estoy muy feliz hoy celebrando con todos ustedes pues voy a dedicarme profesionalmente a la música que es mi pasión. Muchas gracias a mi familia por todo su amor y ser mi ejemplo. Muchas gracias a Universal Music por confiar en mí y muchas gracias a los medios de comunicación por todo su apoyo. Estoy muy emocionada".


        </p>

        <p>
            Camila Fernández será representada por Infinity. Empresa que resulta de la unión de Universal Music y Grupo Fernández.


        </p>

